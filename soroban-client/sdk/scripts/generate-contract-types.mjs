import fs from "node:fs";
import path from "node:path";

const repoRoot = path.resolve(process.cwd());
const contractRoot = path.resolve(repoRoot, "..", "soroban-contract", "contracts");
const outputFile = path.resolve(repoRoot, "sdk", "src", "generated", "contracts.ts");

const CONTRACT_FILES = [
  ["eventManager", "event_manager/src/lib.rs"],
  ["ticketFactory", "ticket_factory/src/lib.rs"],
  ["ticketNft", "ticket_nft/src/lib.rs"],
  ["tbaRegistry", "tba_registry/src/lib.rs"],
  ["tbaAccount", "tba_account/src/lib.rs"],
];

function normalizeType(rustType) {
  return rustType
    .replace(/\s+/g, " ")
    .replace(/Result<([^,>]+),[^>]+>/g, "$1")
    .trim();
}

function parseMethods(source) {
  const methodRegex = /pub fn ([a-zA-Z0-9_]+)\(([\s\S]*?)\)\s*(?:->\s*([^{]+))?\s*\{/g;
  const methods = [];
  for (const match of source.matchAll(methodRegex)) {
    const [, name, rawArgs, rawReturn] = match;
    const args = rawArgs
      .split(",")
      .map((part) => part.trim())
      .filter((part) => part && !/^env:\s*Env$/.test(part))
      .map((part) => {
        const [argName, ...typeParts] = part.split(":");
        return {
          name: argName.trim(),
          type: normalizeType(typeParts.join(":")),
        };
      });
    methods.push({
      name,
      args,
      returns: normalizeType(rawReturn || "()"),
      mutates:
        name.startsWith("get_") ||
        name === "owner_of" ||
        name === "balance_of" ||
        name === "is_valid" ||
        name === "token" ||
        name === "token_contract" ||
        name === "token_id" ||
        name === "owner" ||
        name === "nonce"
          ? false
          : true,
    });
  }
  return methods;
}

function parseErrors(source) {
  const enumBlock = source.match(/pub enum Error\s*\{([\s\S]*?)\n\}/);
  if (!enumBlock) return [];
  return enumBlock[1]
    .split("\n")
    .map((line) => line.trim())
    .filter((line) => line && line.includes("="))
    .map((line) => {
      const cleaned = line.replace(/,$/, "");
      const [name, code] = cleaned.split("=").map((part) => part.trim());
      return { name, code: Number(code) };
    });
}

const specs = Object.fromEntries(
  CONTRACT_FILES.map(([contractName, relativePath]) => {
    const filePath = path.resolve(contractRoot, relativePath);
    const source = fs.readFileSync(filePath, "utf8");
    return [
      contractName,
      {
        contract: contractName,
        source: path.relative(repoRoot, filePath).replace(/\\/g, "/"),
        methods: parseMethods(source),
        errors: parseErrors(source),
      },
    ];
  })
);

const content = `/* eslint-disable */
// This file is auto-generated by sdk/scripts/generate-contract-types.mjs.

export type GeneratedContractName =
  | "eventManager"
  | "ticketFactory"
  | "ticketNft"
  | "tbaRegistry"
  | "tbaAccount";

export interface GeneratedMethodArg {
  readonly name: string;
  readonly type: string;
}

export interface GeneratedMethodSpec {
  readonly name: string;
  readonly args: readonly GeneratedMethodArg[];
  readonly returns: string;
  readonly mutates: boolean;
}

export interface GeneratedErrorSpec {
  readonly name: string;
  readonly code: number;
}

export interface GeneratedContractSpec {
  readonly contract: GeneratedContractName;
  readonly source: string;
  readonly methods: readonly GeneratedMethodSpec[];
  readonly errors: readonly GeneratedErrorSpec[];
}

export const GENERATED_CONTRACT_SPECS = ${JSON.stringify(specs, null, 2)} as const;
`;

fs.mkdirSync(path.dirname(outputFile), { recursive: true });
fs.writeFileSync(outputFile, content);
